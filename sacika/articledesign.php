<div id="title">Konsep Baru Nissan NV350 Caravan Memungkinkan Pengguna Bekerja Dari Mana Saja</div>
<div id="article_date">2 September 2018</div>
<div id="content">Selama pandemi, kegiatan work from home bisa saja menjenuhkan. Ada kalanya kita ingin bekerja di luar untuk mencari suasana baru, terutama di alam bebas, dan tentu saja pastinya ingin tetap aman. Keinginan-keinginan yang muncul di tengah pandemi seperti ini akhirnya mendorong sebuah produsen otomotif Nissan untuk mendorong tren bekerja dari mana saja dengan menciptakan NV350 Caravan Office Pod.
<br><br>
Kendaraan ini dirancang oleh Nissan untuk para profesional dan penggemar alam terbuka. NV350 Caravan Office Pod dilengkapi dengan ruang kerja yang dapat dikontrol oleh smartphone agar bisa ditarik keluar dari belakang van. Tidak perlu khawatir akan hujan, ruang kerja ini pastinya akan dinaungi oleh pintu bagasi mobil yang berperan sebagai atap. Selain itu, di bagian dalam mobil terdapat pula workstation model terkini dengan komputer dan kursi kantor Herman Miller.
<br><br>
Pada eksterior kendaraan terdapat spakbor di bagian depan dan belakang serta body graphic untuk mengekspresikan “serba bisa” dalam menempuh medan on-road dan off-road. Di bagian atas mobil, Nissan menyertakan balkon yang bisa diakses dari dalam ruangan dengan tujuan agar para pekerja dapat bersantai di waktu senggang. Untuk melihat desain dari NV350 Caravan Office Pod secara rinci, silahkan memutar video di bawah ini.</div>
